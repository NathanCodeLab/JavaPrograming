package com.programming;

public class SwapString {

	public static void main(String[] args) {

		String a="sabari";
		String b="nathan";
		

		String temp;

		System.out.println("Before swapping : " + a + " " + b);

		temp = a;
		a = b;
		b = temp;
		System.out.println("After swapping : " + a + " " + b);

		
		
		
		
	}

}
