package com.OOPsConcept;

public class EncapsulationConcept {
	
	
	public String str="Nathan";
	public int num=10;
	public float flt=20.20f;
	public String getStr() {
		return str;
	}
	public void setStr(String str) {
		this.str = str;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public float getFlt() {
		return flt;
	}
	public void setFlt(float flt) {
		this.flt = flt;
	}
	
	

}
